package heroes.juego;

public class Heroe {

  public final int vida;
  public final int rango;
  public final int carga;
  public final int velocidad;

  public Heroe (int vida, int rango, int carga, int velocidad) {
    this.vida = vida;
    this.rango = rango;
    this.carga = carga;
    this.velocidad = velocidad;
  }

}
